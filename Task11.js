var tempCelsius=prompt('Enter temperature in Celsius');
var farhen=(tempCelsius*9/5)+32;
document.write(tempCelsius + '<sup>o</sup>C is '+farhen+'<sup>o</sup>F <br>');
var tempFahren=prompt('Enter temperature in Fahrenheit');
var celsius=(tempFahren-32)*5/9;
document.write(tempFahren + '<sup>o</sup>F is '+celsius+'<sup>o</sup>C <br>');

