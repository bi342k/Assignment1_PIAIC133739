var a=parseInt(prompt('Please enter value '));
var b=a;
document.write('Result <br> value of a is: ' + b + '<br>');
document.write('..............................................<br><br>');
b=++a;
document.write('The value ++a is: '+b+
'<br> Now the value of a is: '+a+'<br><br>')
b=a++;
document.write('The value a++ is: '+b+
'<br> Now the value of a is: '+a+'<br><br>')

b=--a;
document.write('The value --a is: '+b+
'<br> Now the value of a is: '+a+'<br><br>')
b=a--;
document.write('The value a-- is: '+b+
'<br> Now the value of a is: '+a+'<br><br>')
